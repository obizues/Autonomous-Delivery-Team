def run_resume_from_dashboard(
    workflow_id: str,
    human_response: str,
    resume_stage: str = "IMPLEMENTATION",
    responder: str = "human_operator",
    resume_max_steps: int = 120,
    response_template: str = "",
) -> tuple[bool, str]:
    resolved_sqlite, checked_paths = _resolve_sqlite_path_for_workflow(workflow_id)
    if resolved_sqlite is None:
        checked = ", ".join(_format_path_for_message(path) for path in checked_paths) if checked_paths else "none"
        return (
            False,
            (
                f"Resume blocked: workflow_id '{workflow_id}' was not found in available SQLite state stores. "
                f"Checked: {checked}. Run escalation from dashboard first, or select a workflow generated by this workspace."
            ),
        )

    try:
        result = subprocess.run(
            [sys.executable, "-m", "ai_software_factory"],
            cwd=BASE_DIR,
            env={
                **os.environ,
                "PYTHONPATH": "src",
                "ASF_PERSISTENCE_BACKEND": "sqlite",
                "ASF_SQLITE_PATH": str(resolved_sqlite),
                "ASF_RESUME_WORKFLOW_ID": workflow_id,
                "ASF_HUMAN_RESPONSE": human_response,
                "ASF_RESUME_STAGE": resume_stage,
                "ASF_RESUME_RESPONDER": responder,
                "ASF_RESUME_MAX_STEPS": str(resume_max_steps),
                "ASF_HUMAN_RESPONSE_TEMPLATE": response_template,
            },
            capture_output=True,
            text=True,
        )
    except Exception as exc:
        return False, f"Failed to resume workflow: {exc}"

    output = ((result.stdout or "") + "\n" + (result.stderr or "")).strip()
    if result.returncode != 0:
        return False, output[-4000:] if output else "Workflow resume failed with no output."
    return True, output[-4000:] if output else "Workflow resumed successfully."


def latest_escalation_reason(events: list[dict]) -> str | None:
    for event in reversed(events):
        if event.get("event_type") != "ESCALATION_RAISED":
            continue
        payload = event.get("payload", {})
        reason = payload.get("reason")
        if isinstance(reason, str) and reason.strip():
            return reason.strip()
    return None
